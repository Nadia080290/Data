export default function TableSearch(props) {

  const {data,fiscaliaService, token} = props
  console.log(data, "en la tabla")
  const [dataTabla, setDataTabla] = React.useState([])
  console.log(dataTabla,"tenemos dataaa")
  const [order, setOrder] = React.useState('asc');
  const [orderBy, setOrderBy] = React.useState('calories');
  const [selected, setSelected] = React.useState([]);
  const [page, setPage] = React.useState(0);
  const [dense, setDense] = React.useState(false);
  const [rowsPerPage, setRowsPerPage] = React.useState(5);
  const [openModal, setOpenModal] = React.useState(false);
  const [causaElegida, setDataCausasElegida] = React.useState("");


   useEffect(() => {
        if (data.length > 0) {
          setTimeout(() => {
            setDataTabla(data)
          }, 1000)
          setTimeout(() => {
            setDataTabla(data)
          }, 3000)
          setTimeout(() => {
            setDataTabla(data)
          }, 5000)
        }
    }, [data]);
 

  const handleClickOpen = (row) => {
    setDataCausasElegida(row)
    setOpenModal(true);
  };

  const handleClose = () => {
    setOpenModal(false);
  };
  const handleRequestSort = (event, property) => {
    const isAsc = orderBy === property && order === 'asc';
    setOrder(isAsc ? 'desc' : 'asc');
    setOrderBy(property);
  };

  const handleSelectAllClick = (event) => {
    if (event.target.checked) {
      const newSelected = dataTabla.map((n) => n.id);
      setSelected(newSelected);
      return;
    }
    setSelected([]);
  };


  const handleChangePage = (event, newPage) => {
    setPage(newPage);
  };

  const handleChangeRowsPerPage = (event) => {
    setRowsPerPage(parseInt(event.target.value, 10));
    setPage(0);
  };

  const isSelected = (id) => selected.indexOf(id) !== -1;

  // Avoid a layout jump when reaching the last page with empty rows.
  const emptyRows =
    page > 0 ? Math.max(0, (1 + page) * rowsPerPage - dataTabla.length) : 0;

  const visibleRows = React.useMemo(
    () =>
      stableSort(dataTabla, getComparator(order, orderBy)).slice(
        page * rowsPerPage,
        page * rowsPerPage + rowsPerPage,
      ),
    [order, orderBy, page, rowsPerPage],
  );

  return (

    <Box sx={{ width: '95%', ml:2, mt: 4}}>
      <Paper sx={{ width: '100%', mb: 2 }}>
        <EnhancedTableToolbar numSelected={selected.length} />
       
        <TableContainer>
          <Table
            sx={{ minWidth: 750 }}
            aria-labelledby="tableTitle"
            size={dense ? 'small' : 'medium'}
            
          >
            <EnhancedTableHead
              numSelected={selected.length}
              order={order}
              orderBy={orderBy}
              onSelectAllClick={handleSelectAllClick}
              onRequestSort={handleRequestSort}
              rowCount={dataTabla.length}
              
            />
            <TableBody>
              {visibleRows.map((row, index) => {
                const isItemSelected = isSelected(row.id);
                const labelId = `enhanced-table-checkbox-${index}`;

                return (
                  <TableRow
                    hover
                    tabIndex={-1}
                    key={row.id}
                    selected={isItemSelected}
                    sx={{ cursor: 'pointer' }}
                  >
                    <TableCell >
                    </TableCell>
                    <TableCell
                      component="th"
                      id={labelId}
                      scope="row"
                      padding="none"
                    >
                      {row.ruc}
                    </TableCell>
                    <TableCell align="right">{row.estado}</TableCell>
                    <TableCell align="right">{row.fecha}</TableCell>
                    <TableCell align="right">{row.codigo_delito}</TableCell>
                    <TableCell align="right">{row.grupo_delito}</TableCell>
                    <TableCell align="right"><AnalyticsIcon sx={{color: "#FA7900"}} onClick={() =>handleClickOpen(row)}/></TableCell>
                  </TableRow>
                );
              })}
              {emptyRows > 0 && (
                <TableRow
                  style={{
                    height: (dense ? 33 : 53) * emptyRows,
                  }}
                >
                  <TableCell colSpan={6} />
                </TableRow>
              )}
            </TableBody>
          </Table>
        </TableContainer>
        <TablePagination
          rowsPerPageOptions={[5, 10, 25]}
          component="div"
          count={dataTabla.length}
          rowsPerPage={rowsPerPage}
          page={page}
          onPageChange={handleChangePage}
          onRowsPerPageChange={handleChangeRowsPerPage}
        />
      </Paper>
      <Modal open={openModal} close={handleClose} data={causaElegida} fiscaliaService={fiscaliaService} token={token}/>
    </Box>
  );
}